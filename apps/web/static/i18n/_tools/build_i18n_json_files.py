def main() -> None:
    en_flat = flatten(load_js_obj(MSG_DIR / "en.js"))
    if len(en_flat) != 680:
        raise SystemExit(f"Expected 680 keys, got {len(en_flat)}")
    if len(TRANSLATIONS) != 680:
        missing = set(en_flat) - set(TRANSLATIONS)
        extra = set(TRANSLATIONS) - set(en_flat)
        raise SystemExit(
            f"Translation count {len(TRANSLATIONS)} != 680; "
            f"missing={len(missing)} extra={len(extra)}; "
            f"sample missing={sorted(missing)[:5]}"
        )

    for locale, idx in (("es", 0), ("fr", 1), ("pt", 2)):
        catalog = {key: triple[idx] for key, triple in TRANSLATIONS.items()}
        out_path = ROOT / f"i18n_{locale}.json"
        out_path.write_text(
            json.dumps(dict(sorted(catalog.items())), ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
        same = sum(1 for k in en_flat if catalog[k] == en_flat[k])
        print(f"{locale}: keys={len(catalog)}, same_as_en={same}")


if __name__ == "__main__":
    main()
